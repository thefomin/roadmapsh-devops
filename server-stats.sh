#Статистика производительности сервера

#Скрипт должен запускаться на любом сервере Linux и предоставлять следующие статистические данные:

#Общее использование ЦП
#Общий объем используемой памяти (свободная и используемая память, включая проценты)
#Общее использование дискового пространства (свободное и используемое, включая проценты)
#5 процессов с наибольшей загрузкой ЦП
#5 процессов с самым высоким потреблением памяти


#Дополнительная цель: 
#При желании можно добавить больше статистических данных, таких как версия ОС, время работы, средняя загрузка, количество авторизованных пользователей, количество неудачных попыток входа и т. д.

#!/bin/bash

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

print_separator() {
    echo -e "${BLUE}═══════════════════════════════════════════════════════════════${NC}"
}

clear
echo -e "${GREEN}"
echo "╔══════════════════════════════════════════════════════════════════╗"
echo "║              СТАТИСТИКА ПРОИЗВОДИТЕЛЬНОСТИ СИСТЕМЫ               ║"
echo "╚══════════════════════════════════════════════════════════════════╝"
echo -e "${NC}"
echo "Время выполнения: $(date)"
print_separator

# 1. ИСПОЛЬЗОВАНИЕ ЦП
echo -e "\n${YELLOW} ИСПОЛЬЗОВАНИЕ CPU:${NC}"
cpu_usage=$(top -bn1 | grep "Cpu(s)" | awk '{print $2}' | cut -d'%' -f1)
if [ -z "$cpu_usage" ]; then
    cpu_usage=$(top -bn1 | grep "%Cpu" | awk '{print $2}')
fi
echo -e "  Загрузка CPU: ${GREEN}${cpu_usage}%${NC}"

# 2. ИСПОЛЬЗОВАНИЕ ПАМЯТИ
echo -e "\n${YELLOW} ИСПОЛЬЗОВАНИЕ ПАМЯТИ:${NC}"
mem_total=$(free -h | awk '/^Mem:/ {print $2}')
mem_used=$(free -h | awk '/^Mem:/ {print $3}')
mem_free=$(free -h | awk '/^Mem:/ {print $4}')
mem_percent=$(free | awk '/^Mem:/ {printf "%.1f", $3/$2 * 100}')

echo -e "  Всего памяти: ${GREEN}${mem_total}${NC}"
echo -e "  Использовано: ${YELLOW}${mem_used}${NC} (${mem_percent}%)"
echo -e "  Свободно:     ${GREEN}${mem_free}${NC}"

# 3. ИСПОЛЬЗОВАНИЕ ДИСКА
echo -e "\n${YELLOW} ИСПОЛЬЗОВАНИЕ ДИСКА:${NC}"
df -h / | awk 'NR==2 {
    total=$2
    used=$3
    free=$4
    percent=$5
    printf "  Всего диска:  \033[0;32m%s\033[0m\n", total
    printf "  Использовано: \033[1;33m%s\033[0m (%s)\n", used, percent
    printf "  Свободно:     \033[0;32m%s\033[0m\n", free
}'

# 4. ТОП 5 ПРОЦЕССОВ ПО ЗАГРУЗКЕ ЦП
echo -e "\n${YELLOW} ТОП 5 ПРОЦЕССОВ ПО ЗАГРУЗКЕ CPU:${NC}"
echo -e "${BLUE}┌─────────┬──────────────────────────────────┬────────────┐${NC}"
echo -e "${BLUE}│  PID    │ ИМЯ ПРОЦЕССА                     │ CPU %      │${NC}"
echo -e "${BLUE}├─────────┼──────────────────────────────────┼────────────┤${NC}"
ps aux --sort=-%cpu | head -6 | tail -5 | awk '{
    printf "│ %-7s │ %-32s │ %4s%%      │\n", $2, substr($11,1,32), $3
}'
echo -e "${BLUE}└─────────┴──────────────────────────────────┴────────────┘${NC}"

# 5. ТОП 5 ПРОЦЕССОВ ПО ИСПОЛЬЗОВАНИЮ ПАМЯТИ
echo -e "\n${YELLOW} ТОП 5 ПРОЦЕССОВ ПО ЗАГРУЗКИ ПАМЯТИ:${NC}"
echo -e "${BLUE}┌─────────┬──────────────────────────────────┬────────────┐${NC}"
echo -e "${BLUE}│  PID    │ ИМЯ ПРОЦЕССА                     │ MEM %      │${NC}"
echo -e "${BLUE}├─────────┼──────────────────────────────────┼────────────┤${NC}"
ps aux --sort=-%mem | head -6 | tail -5 | awk '{
    printf "│ %-7s │ %-32s │ %4s%%      │\n", $2, substr($11,1,32), $4
}'

echo -e "${BLUE}└─────────┴──────────────────────────────────┴────────────┘${NC}"

print_separator

# Дополнительная информация о системе
echo -e "\n${GREEN} СИСТЕМНАЯ ИНФОРМАЦИЯ:${NC}"
echo -e " ИМЯ ХОСТА:   $(hostname)"
echo -e " ВРЕМЯ РАБОТЫ:     $(uptime -p | sed 's/up //')"
echo -e " ПОЛЬЗОВАТЕЛИ:      $(who | wc -l) активных пользователей"

# Средняя загрузка системы
loadavg=$(uptime | awk -F'СРЕДНЯЯ НАГРУЗКА:' '{print $2}')
echo -e "  СРЕДНЯЯ ЗАГРУЗКА:   ${loadavg}"

print_separator
echo -e "\n${GREEN}✅ Статистика собрана успешно!${NC}\n"
